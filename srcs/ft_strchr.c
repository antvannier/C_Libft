#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (s[i] == (char)c)
			return (i + (char *)s);
		i++;
	}
	if ((char)c == '\0')
		return (i + (char *)s);
	return ((char *)NULL);
}
