int		ft_sizeint(int n)
{
	int i;

	i = 0;
	if (n > -2147483648 && n <= 2147483647)
	{
		if (n < 0)
		{
			i++;
			n = -n;
		}
		while (n / 10 >= 10)
		{
			i++;
			n = n / 10;
		}
		i++;
	}
	if (n == -2147483648)
		i = 11;
	return (i);
}
