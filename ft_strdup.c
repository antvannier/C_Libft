#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		size;
	char	*dest;
	int		i;

	size = ft_strlen(s1);
	dest = (char*)malloc(sizeof(*dest) * size);
	i = 0;
	if (!dest)
		return (dest);
	while (s1[i])
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
